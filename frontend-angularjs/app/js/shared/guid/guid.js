(function () {
    "use strict";

    /**
     * Generates a guid
     * @returns {string}
     */
    function guid () {
        /**
         * Helper Function that generates a random string
         * @returns {string}
         */
        function s4 () {
            return Math.floor((1 + Math.random()) * 0x10000)
                .toString(16)
                .substring(1);
        }

        return s4() + s4() + '-' + s4() + '-' + s4() + '-' + s4() + '-' + s4() + s4() + s4();
    }

    window.guid = guid;
})();
